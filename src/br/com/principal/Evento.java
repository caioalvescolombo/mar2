
package br.com.principal;

public class Evento {
    private int idvento;
    private String nome;
    private double data;

    public int getIdvento() {
        return idvento;
    }

    public void setIdvento(int idvento) {
        this.idvento = idvento;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getData() {
        return data;
    }

    public void setData(double data) {
        this.data = data;
    }
    
}
